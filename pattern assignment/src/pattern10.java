
public class pattern10 {

	public static void main(String[] args) {
		for(int i=5;i>=1;i--)
		{    
			for(int j=i-1;j>=1;j--)
			{
				System.out.print(" ");
			}
			for(int k=i;k<=5;k++)
			{
				System.out.print(k);
			
			}
			System.out.println();
		}
	}

}/* this is really a toughand out of box usaully we make for loop in incremental form 
    but here we have to male it as a decremental statement so keep this program in your mind */
